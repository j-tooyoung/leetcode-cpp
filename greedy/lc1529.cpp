#include "bits/stdc++.h"
using namespace std;
class Solution {
public:
    int minFlips(string target) {
        int n = target.size();
        bool flag = false;
        int cnt = 0;
        for (int i = 0; i < n; ++i) {
            if (!target[i]) {
                continue;
            }


        }
        return cnt;
    }
};